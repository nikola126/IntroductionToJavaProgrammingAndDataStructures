package com.programming.java.test.classes;

public class Point {
    private final int x;
    private final int y;

    @Override
    public boolean equals(Object obj) {
        return super.equals(obj);
    }

    public Point(int x, int y) {
        super();
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
